from __future__ import annotations

"""
命令行脚本：执行完整训练流水线。

这个脚本是整个项目的“总入口”：
1. 读取原始数据
2. 合并人类/机器文本
3. 划分训练集、验证集、测试集
4. 保存切分后的数据
5. 训练模型
6. 导出模型与评估结果
"""

# argparse：
# Python 标准库中的命令行参数解析模块
# 可以让我们在终端中传入参数，例如：
# python run_pipeline.py --seed 123
import argparse

# Path：
# pathlib 模块中的路径对象
# 比字符串路径更安全、更方便跨平台
from pathlib import Path

# sys：
# Python 解释器相关功能
# 这里主要用于修改模块搜索路径
import sys


# -------------------------------------------------------------------
# 项目根目录定位
# -------------------------------------------------------------------


ROOT = Path(__file__).resolve().parents[1]


# insert(0, xxx)
# 插入到最前面，优先搜索
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))


# -------------------------------------------------------------------
# 导入数据预处理模块
# -------------------------------------------------------------------

# load_and_merge：
# 读取人类文本 CSV 和机器文本 CSV
# 并合并成统一 DataFrame
#
# save_split：
# 保存 train/valid/test 三个数据集
#
# split_dataset：
# 按比例切分数据集
from src.data.preprocess import (
    load_and_merge,
    save_split,
    split_dataset,
)


# -------------------------------------------------------------------
# 导入模型训练模块
# -------------------------------------------------------------------

# train_and_save：
# 执行：
# 1. 特征提取
# 2. 模型训练
# 3. 模型评估
# 4. 模型保存
# 5. 输出报告
from src.models.train import train_and_save


# ===================================================================
# 命令行参数定义
# ===================================================================

def parse_args() -> argparse.Namespace:
    """
    定义训练脚本命令行参数。

    返回：
        argparse.Namespace
        一个保存所有参数的对象

    例如：
        args.seed
        args.train_size
    """

    # 创建命令行解析器
    parser = argparse.ArgumentParser(
        description="训练检测模型全流程"
    )

    # ---------------------------------------------------------------
    # 人类文本 CSV 路径
    # ---------------------------------------------------------------
    parser.add_argument(
        "--human-csv",
        type=str,
        default="data/raw/human_texts.csv",
        help="人类文本 CSV 路径"
    )

    # ---------------------------------------------------------------
    # 机器文本 CSV 路径
    # ---------------------------------------------------------------
    parser.add_argument(
        "--machine-csv",
        type=str,
        default="data/raw/machine_texts.csv",
        help="机器文本 CSV 路径"
    )

    # ---------------------------------------------------------------
    # 训练集比例
    # ---------------------------------------------------------------
    parser.add_argument(
        "--train-size",
        type=float,
        default=0.8,
        help="训练集比例"
    )

    # ---------------------------------------------------------------
    # 测试集比例
    # ---------------------------------------------------------------
    parser.add_argument(
        "--test-size",
        type=float,
        default=0.2,
        help="测试集比例"
    )

    # ---------------------------------------------------------------
    # 随机种子
    # ---------------------------------------------------------------
    # 控制随机行为
    # 保证每次运行结果一致
    #
    # 常见用途：
    # - 数据集切分
    # - 模型初始化
    parser.add_argument(
        "--seed",
        type=int,
        default=42,
        help="随机种子"
    )

    # ---------------------------------------------------------------
    # 训练产物输出目录
    # ---------------------------------------------------------------
    # 用于保存：
    # - 模型文件
    # - 图表
    # - 报告
    parser.add_argument(
        "--artifacts-dir",
        type=str,
        default="artifacts",
        help="训练产物输出目录"
    )

    # ---------------------------------------------------------------
    # 数据切分输出目录
    # ---------------------------------------------------------------
    parser.add_argument(
        "--processed-dir",
        type=str,
        default="data/processed",
        help="切分数据输出目录"
    )

    # 解析命令行参数并返回
    return parser.parse_args()


# ===================================================================
# 主流程
# ===================================================================

def main() -> None:
    """
    执行完整训练流程。

    流程如下：

    1. 读取命令行参数
    2. 检查输入文件是否存在
    3. 加载并合并数据
    4. 划分数据集
    5. 保存切分数据
    6. 训练模型
    7. 导出模型与结果
    """

    # ---------------------------------------------------------------
    # 解析命令行参数
    # ---------------------------------------------------------------
    args = parse_args()

    # 项目根目录
    root = ROOT

    # ---------------------------------------------------------------
    # 拼接完整路径
    # ---------------------------------------------------------------

    # 人类文本 CSV 完整路径
    human_csv = root / args.human_csv

    # 机器文本 CSV 完整路径
    machine_csv = root / args.machine_csv

    # ---------------------------------------------------------------
    # 检查文件是否存在
    # ---------------------------------------------------------------

    # exists()
    # 判断文件是否存在
    if not human_csv.exists() or not machine_csv.exists():

        raise FileNotFoundError(
            "找不到输入 CSV。"
            "请先运行 build_real_dataset.py 或 build_demo_dataset.py。"
        )

    # ---------------------------------------------------------------
    # 加载并合并数据
    # ---------------------------------------------------------------

    # 返回一个 DataFrame
    #
    # 结构类似：
    #
    # text                label
    # --------------------------
    # "你好..."             0
    # "这是 AI 文本..."      1
    #
    # label:
    # 0 -> human
    # 1 -> machine
    df = load_and_merge(
        human_csv,
        machine_csv
    )

    # ---------------------------------------------------------------
    # 数据集切分
    # ---------------------------------------------------------------

    # split_dataset 返回 SplitResult
    #
    # 包含：
    # split.train  → 80% 训练集（交叉验证调参）
    # split.test   → 20% 测试集（最终评估）
    split = split_dataset(
        df,

        # 训练集比例
        train_size=args.train_size,

        # 测试集比例
        test_size=args.test_size,

        # 随机种子
        random_state=args.seed,
    )

    # ---------------------------------------------------------------
    # 保存切分后的数据
    # ---------------------------------------------------------------

    # save_split 返回：
    #
    # train_csv
    # test_csv
    #
    # 即两个 CSV 文件路径
    train_csv, test_csv = save_split(
        split,
        root / args.processed_dir
    )

    # ---------------------------------------------------------------
    # 训练模型并保存
    # ---------------------------------------------------------------

    # train_and_save 内部通常包括：
    #
    # 1. 读取 train/test
    # 2. 文本向量化
    # 3. 模型训练 + 5折交叉验证调参
    # 4. 测试集评估
    # 5. 导出模型
    # 6. 导出图表与报告
    train_and_save(
        train_csv,
        test_csv,
        root / args.artifacts_dir
    )

    # ---------------------------------------------------------------
    # 训练结束提示
    # ---------------------------------------------------------------
    print("全流程完成。")


# ===================================================================
# Python 程序入口
# ===================================================================

# __name__
# 当前模块名
#
# 当文件被“直接运行”时：
# __name__ == "__main__"
#
# 当文件被“导入”时：
# __name__ == "模块名"
#
# 作用：
# 防止 import 时自动执行 main()
if __name__ == "__main__":
    main()